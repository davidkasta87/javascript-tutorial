let edad = 18; //identifica como un número
let cantidad = 20; //identifica como un string

if (edad > cantidad) {
  console.log("Edad es mayor que cantidad");
} else if (edad === cantidad) {
  console.log("Edad es igual a cantidad");
} else if (edad < cantidad) {
  console.log("Edad es menor que cantidad");
} else {
  console.log("Ninguna de las condiciones se cumplió");
}
